package org.example.model.enums;

/**
 * Enum untuk tombol keyboard yang digunakan dalam game
 */
public enum Key {
    W, // Gerak atas
    A, // Gerak kiri
    S, // Gerak bawah
    D, // Gerak kanan
    F, // Pick Up / Drop (Transfer Item)
    V, // Interact / Action (Process like Cut, Wash)
    P, // Pause
    TAB, // Ganti chef
    SPACE // Dash
}
